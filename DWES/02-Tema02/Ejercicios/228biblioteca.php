<?php
    // Funcion para sumar
    function sumar($num1,$num2){
        return $num1+$num2;
    }

    // Funcion para restar
    function restar($num1,$num2){
        return $num1-$num2;
    }

    // Funcion para multiplicar
    function multiplicar($num1,$num2){
        return $num1*$num2;
    }

    // Funcion para dividir
    function dividir($num1,$num2){
        return $num1/$num2;
    }
?>